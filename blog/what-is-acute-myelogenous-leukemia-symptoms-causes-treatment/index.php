        <h3>When should you see a doctor?</h3>
        <p>Make an appointment with a doctor or primary care physician if you develop any signs or symptoms which seem unusual or that worry you.</p>
        <h3>Acute Myelogenous Leukemia Causes</h3>
        <p>Acute myeloid leukaemia occurs when a bone marrow cell develops, alters or mutates its genetic material or DNA. A cell's DNA consists of the instructions which tell a cell what to do. Generally, the DNA tells the cell to grow at a set rate and to die at a set time. In acute myelogenous leukemia, the mutations instruct the bone marrow cell to continue growing and dividing.</p>
        <p>When this occurs, blood cell production gets out of hand. The bone marrow produces immature cells which develop into leukemic white blood cells known as myeloblasts. These abnormal cells are incapable of working properly, and they could accumulate and displace healthy cells.</p>
        <p>It is not clear what causes the DNA mutations which lead to leukemia, but primary care physicians have identified factors which increase the risk.</p>
