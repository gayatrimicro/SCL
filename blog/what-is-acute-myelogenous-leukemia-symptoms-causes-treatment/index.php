        <h3 class="marT28">Acute Myelogenous Leukemia Diagnosis</h3>
        <p>If you have signs or symptoms of acute myelogenous leukemia, your primary care physician might suggest that you undergo diagnostic tests, including:</p>
        <ul class="ListItem">
          <li><b>Blood tests – </b>Most of the people with acute myelogenous leukemia have too many white blood cells, not enough red blood cells and not enough platelets. But sometimes the level of white blood cells could be too low. The presence of blast cells, immature cells generally found in bone marrow but not circulating in the blood is another indicator of acute myelogenous leukemia.</li>
          <li><b>Bone marrow test - </b>A blood test could indicate leukemia, but it generally takes a bone marrow test to confirm the diagnosis.<br>During a bone marrow biopsy, a syringe is used to remove a specimen of your bone marrow. Generally, the specimen is taken from your hip bone. The specimen goes to a laboratory for testing.</li>
          <li><b>Lumbar puncture (spinal tap) - </b> In certain situations, it might be required to remove some of the fluid around your spinal cord to check for leukemia cells. Your primary care physician could collect this fluid by inserting a small syringe into the spinal canal in your lower back.</li>
          <li><b>Testing your cancer cells in the laboratory - </b>In a laboratory, primary care physicians test your leukemia cells to better understand which gene mutations are present. This could help determine your prognosis and guide your treatment.</li>
        </ul><br>
        <p>If your primary care physician suspects leukemia, you might be referred to a primary care physician who specializes in blood cancer hematologist or medical oncologist.</p>
