    <h3 class="h3bgh">Acute Myelogenous Leukemia Treatment</h3>
    <p>Treatment of acute myelogenous leukemia depends upon several factors, including the subtype of the disease, your age, your overall health and your preferences.</p>
    <p><b>In general, treatment falls into two stages:</b></p>
    <ul class="Comul">
          <li><p><b>Remission induction therapy - </b>The purpose of the first stage of treatment is to kill the leukemia cells in your blood and bone marrow. Although, remission induction generally does not wipe out all of the leukemia cells, so you require further treatment to stop the disease from returning.</p></li>
          <li><p><b>Consolidation therapy - </b>Also called post-remission therapy or maintenance therapy, this stage of treatment is aimed at destroying the remaining leukemia cells. It is considered important to decrease the risk of relapse.</p></li>
        </ul><br>
    <p><b>Therapies used in these stages include:</b></p>

    <ul class="Comul">
          <li><p><b>Chemotherapy - </b>Chemotherapy is the major form of remission induction therapy, though it could also be used for consolidation therapy. Chemotherapy uses chemicals to kill off cancerous cells in your body.<br>People with acute myelogenous leukemia (AML) usually stay in the hospital during chemotherapy treatments because the drugs destroy many normal blood cells in the process of killing leukemia cells. If the first cycle of chemotherapy does not cause remission, it could be repeated.</p></li>
          <li><p><b>Targeted therapy - </b>Targeted drug treatments focus on certain abnormalities present within cancer cells. By blocking these abnormalities, targeted drug treatments have the potential to kill cancer cells. Your leukemia cells will be tested to see if targeted therapy might be helpful for you. Targeted therapy could be used alone or in combination with chemotherapy for induction and consolidation therapy.</p></li>
          <li><p><b>Bone marrow transplant - </b>A bone marrow transplant, also known as a stem cell transplant, might be used for consolidation therapy. A bone marrow transplant helps re-establish healthy stem cells by replacing unhealthy bone marrow with leukemia-free stem cells which will regenerate healthy bone marrow.<br>Before doing a bone marrow transplant, you receive very high doses of chemotherapy or radiation therapy to destroy your leukemia-producing bone marrow. Then you receive infusions of stem cells from a compatible donor or allogeneic transplant.<br>You could also receive your own stem cells or autologous transplant if you were previously in remission and had your healthy stem cells removed and stored for a future transplant.</p></li>
          <li><p><b>Clinical trials - </b>Some people with leukemia choose to participate in clinical trials to try experimental treatments or new combinations of known therapies.</p></li>
        </ul>

