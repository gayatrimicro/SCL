    <h3 class="h3bgh">Risk factors</h3>
    <p>Factors which might increase your risk of acute myelogenous leukemia (AML) involve:</p>
    <ul class="ListItem">
      <li><b>Increasing age - </b>The risk of acute myelogenous leukemia increases as you get older. Acute myelogenous leukemia is more common in adults age 65 and older.</li>
      <li><b>Your sex - </b>Men are more likely to develop acute myelogenous leukemia as compared to women.</li>
      <li><b>Previous cancer treatment - </b>People who have had specific types of chemotherapy and radiation therapy might have a greater risk of developing AML.</li>
      <li><b>Exposure to radiation - </b>People exposed to very high levels of radiation, like survivors of a nuclear reactor accident, have a higher risk of developing AML.</li>
      <li><b>Dangerous chemical exposure - </b>Exposure to specific chemicals, like benzene, is linked to a greater risk of AML.</li>
      <li><b>Smoking - </b>AML is linked with cigarette smoke, that contains benzene and other known carcinogenic chemicals.</li>
      <li><b>Other blood disorders - </b>People who have had another blood disorder, like myelodysplasia, myelofibrosis, polycythemia vera or thrombocythemia, are at greater risk of developing AML.</li>
      <li><b>Genetic disorders - </b>Specific genetic disorders, like Down syndrome, are related with an increased risk of AML.</li>
    </ul><br>
    <p>Many people with acute myelogenous leukemia (AML) have no known risk factors, and many people who have risk factors never develop the cancer.</p>
