    <h1>Acute Myelogenous Leukemia</h1>
    <h3 class="h3bgh">What is Acute Myelogenous Leukemia?</h3>
    <p><a href="http://specialtycarelive.com/orthopedics/"><b>Acute myelogenous leukemia (AML)</b></a> is a cancer of the blood and bone marrow the spongy tissue within the bones where blood cells are formed.</p>
    <p>The word "acute" in acute myelogenous leukemia signifies the disease's rapid progression. It is known as myelogenous leukemia because it affects a group of white blood cells known as the myeloid cells, which generally develop into the various kinds of matured blood cells, for instance red blood cells, white blood cells and platelets.</p>
    <p>Acute myelogenous leukemia is also called as acute myeloid leukemia, acute myeloblastic leukemia, acute granulocytic leukemia and acute nonlymphocytic leukemia.</p>
